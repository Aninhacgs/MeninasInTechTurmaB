
//Valor de PI
var x = Math.PI;
console.log("Valor de PI -> "+x);

//Arrendontar um valor para baixo
var y = Math.floor(2.9);
console.log("Valor arredondado para baixo de 2.9 -> "+y);

//Arrendontar um valor para cima
var w = Math.ceil(2.9);
console.log("Valor arredondado para cima de 2.9 -> "+w);

//Calculando a raiz quadrada de um número
var raiz = Math.sqrt(36);
console.log("Raiz quadrada do valor 36 -> "+raiz);

//Calculano potenciação
var pot = Math.pow(2,3);
console.log("Exponecial de 2**3 -> "+pot);

//Calculando ângulos

var seno = Math.sin(60);
console.log("Seno do ângulo de 60 -> "+seno);

var tangente = Math.tan(60);
console.log("Tangente do ângulo de 60 ->"+tangente);

var cosseno = Math.cos(60);
console.log("Cosseno do ângulo de 60 -> "+cosseno);

//Valor truncado

var trunc = Math.trunc(5.9);
console.log("Valor truncado de 5.9 -> "+trunc);

//Calculando logaritmo

var log = Math.log(2);
console.log("Logaritmo de 2 -> "+log);

var log2 = Math.log2(2);
console.log("Logaritmo de 2 na base 2 -> "+log2);

var log10 = Math.log10(2);
console.log("Logaritmo de 2 na base 10 -> "+log10);